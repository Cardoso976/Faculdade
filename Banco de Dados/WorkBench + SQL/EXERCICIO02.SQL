CREATE TABLE engenheiro (

	CREA FLOAT PRIMARY KEY NOT NULL,
	NOME VARCHAR(45) NOT NULL,
	E_MAIL VARCHAR(45) NOT NULL,
	TELEFONE VARCHAR(45) NOT NULL
);

CREATE TABLE funcionario (

	CPF INT PRIMARY KEY NOT NULL,
	TEL_FIXO VARCHAR(45) NOT NULL,
	TEL_CEL VARCHAR(45) NOT NULL,
	CARGA_HORARIA DECIMAL(9,2) NOT NULL,
	FUNCIONALIDADE VARCHAR(45) NOT NULL,
	NOME VARCHAR(45) NOT NULL
);

CREATE TABLE projeto (

	NUM_PROJETO INT NOT NULL,
	DATA_INICIO DATE NOT NULL,		
	DATA_FIM DATE NOT NULL,
	DESCRICAO VARCHAR(200) NOT NULL,
	NOME_PROJETO VARCHAR(45) NOT NULL,
	ENGENHEIRO_CREA FLOAT,
	PRIMARY KEY(NUM_PROJETO,ENGENHEIRO_CREA), 
	INDEX PROJETO_FKIndex1 (ENGENHEIRO_CREA)
);

CREATE TABLE Projeto_has_Funcionario(

	FUNCIONARIO_CPF VARCHAR(20) NOT NULL,
	PROJETO_NUM_PROJETO INTEGER NOT NULL,
	PROJETO_ENGENHEIRO_CREA FLOAT NOT NULL,	
	DT_ENTRADA DATE NOT NULL,
	DESCRICAO_TAREFA VARCHAR(45) NOT NULL,
	TEMPO_SERVICE DECIMAL(9,2) NOT NULL,
	INDEX FUNCIONARIO_HAS_PROJETO_FKIndex1(FUNCIONARIO_CPF),
	INDEX FUNCIONARIO_HAS_PROJETO_FKIndex2(PROJETO_NUM_PROJETO, PROJETO_ENGENHEIRO_CREA)
);